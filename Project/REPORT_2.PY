'''
학과 : 컴퓨터공학부
학번 :
설명 : 하나의 정수를 입력으로 받아서 이 정수가 2로 나누어지는지, 3으로 나누어지는지, 혹은 두 정수 모두로 나누어지는지 알려주는 프로그램 작성
'''
#하나의 정수를 입력받는다.
num = int(input("정수를 입력하시오 : "))\

#만약 정수가 2와 3 모두로 나누어지면 
    #입력된 정수는 2와 3으로 나누어집니다.
if num % 2 == 0 and num % 3 == 0:
    print("입력된 정수는 2와 3으로 나누어집니다.")

#아니고 만약에 정수가 2로 나누어 진다면 
    #입력된 정수는 2로 나누어집니다.
if num % 2 == 0:
    print("입력된 정수는 2로 나누어집니다.")

#아니고 만약에 정수가 3으로 나누어진다면   
    #입력된 정수는 3으로 나누어집니다.
if num % 3 == 0:
    print("입력된 정수는 3으로 나누어집니다.")

#아니면 
    #입력된 정수는 2와 3으로 나누어지지 않습니다.
else:
    print("입력된 정수는 2와 3으로 나누어지지 않습니다.")